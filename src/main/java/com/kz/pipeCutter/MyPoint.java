package com.kz.pipeCutter;

public class MyPoint {

	/**
	 * @param args
	 */

	int id;
	int surface;
	int edge;
	float x;
	float y;
	float z;
	
	public MyPoint(int id, int surface, int edge, float x, float y, float z)
	{
		this.id = id;
		this.surface = surface;
		this.edge = edge;
		this.x =x;
		this.y=y;
		this.z=z;
	}
}
